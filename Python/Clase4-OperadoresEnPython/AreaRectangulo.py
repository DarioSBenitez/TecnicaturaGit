print("Se desea calcular el área y el perimetro de un rectangulo")
print("")

alto = int(input("Ingresa el alto del rectangulo:"))
ancho = int(input("Ingresa el ancho: "))
area = alto * ancho
perimetro = (alto - ancho) * 2
print(f'El área es: {area}')
print(f"El perímetro es: {perimetro}")

