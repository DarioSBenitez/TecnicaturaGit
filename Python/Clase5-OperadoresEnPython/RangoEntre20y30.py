edad = int(input("Digite su edad: "))
veinte = edad >= 20 and edad < 30
print(veinte)
treinta = edad >= 30 and edad < 40
print(treinta)

if veinte or treinta:
    print("Estas en el rango de (20 a 29) o (30 a 39) años")
else:
    print('No estas en el rango de (20\'0) 0 (30\'0) años')

