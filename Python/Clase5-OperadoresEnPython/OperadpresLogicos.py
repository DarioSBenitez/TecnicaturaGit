# Operadores logicos

# Operador and
a = True
b = True
resultado = a and b
print(resultado)

a = False
b = True
resultado = a and b
print(resultado)

# Operador or
resultado = a or b
print(resultado)

# Para que el or sea falso los dos deben ser falsos
a = False
b = False
resultado = a or b
print(resultado)